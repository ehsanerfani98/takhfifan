<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Open the first FAQ item by default
        const firstFaq = document.getElementById('faq-1');
        firstFaq.style.maxHeight = firstFaq.scrollHeight + 20 + 'px';
        const firstArrow = firstFaq.previousElementSibling.querySelector('img');
        firstArrow.classList.remove('rotate-180');

        // Set first item as active
        const firstFaqItem = firstFaq.closest('.py-1.my-3');
        firstFaqItem.classList.add('bg-[#EBF0F4]');
        const firstQuestion = firstArrow.closest('button').querySelector('p');
        firstQuestion.classList.add('text-blue-700');
        firstQuestion.classList.remove('text-gray-900');

        // Add click event to all FAQ buttons
        const faqButtons = document.querySelectorAll('.py-1.my-3 > button');

        faqButtons.forEach(button => {
            button.addEventListener('click', () => {
                const content = button.nextElementSibling;
                const arrow = button.querySelector('img');
                const faqItem = button.closest('.py-1.my-3');
                const question = button.querySelector('p');

                // Toggle max-height for the content
                if (content.style.maxHeight && content.style.maxHeight !== '0px') {
                    // Collapsing
                    content.style.maxHeight = '0px';
                    arrow.classList.add('rotate-180');

                    // Reset styles
                    faqItem.classList.remove('bg-[#EBF0F4]');
                    faqItem.classList.add('bg-white');
                    question.classList.remove('text-blue-700');
                    question.classList.add('text-gray-900');
                } else {
                    // Expanding
                    content.style.maxHeight = content.scrollHeight + 'px';
                    arrow.classList.remove('rotate-180');

                    // Apply active styles
                    faqItem.classList.remove('bg-white');
                    faqItem.classList.remove('bg-[#FFFFFF]');
                    faqItem.classList.add('bg-[#EBF0F4]');
                    question.classList.remove('text-gray-900');
                    question.classList.add('text-blue-700');
                }
            });
        });
    });
</script>
